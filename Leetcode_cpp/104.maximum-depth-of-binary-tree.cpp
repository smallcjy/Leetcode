/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
void find_max(TreeNode* root,int &max,int& MAX){
    if(root==NULL){
        return;
    }
    if(root->left==NULL&&root->right==NULL){
        max++;
        if(max>MAX)MAX = max;
        max--; 
        return;  
    }
    max++;
    find_max(root->left,max,MAX);
    find_max(root->right,max,MAX);
    max--;
}

class Solution {
public:
    int maxDepth(TreeNode* root) {
       int max = 0;
       int MAX = 0;
       find_max(root,max,MAX);
       return MAX;
    }
};
