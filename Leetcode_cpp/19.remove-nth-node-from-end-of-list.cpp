class Solution
{
public:
//指针列表的删除需要单独考虑删除头指针
    ListNode *removeNthFromEnd(ListNode *head, int n)
    {
        unordered_map<int, ListNode*> map;
        ListNode* cur = head;
        int i = 1;
        while(cur!=nullptr) {
            map.emplace(i, cur);
            cur = cur->next;
            i++;
        }
        i--;
        std::cout<<i;
        int pos = i - n;
        if(pos == 0) {
           head = head->next;
        }
        else {
            ListNode* node = map.at(pos);
            node->next = node->next->next;
        }

        return head;
    }
};
